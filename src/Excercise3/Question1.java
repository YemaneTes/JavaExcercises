//Write a java program to calculate the sum of first 10 integers. The program should calculate the sum using for loop

package Excercise3;

public class Question1 {

	public static void main(String[] args) {
		
		int i, num = 10, sum = 0;  
		for(i = 1; i <= num; ++i)  
			
		{  
		sum = sum + i;  
		}  
		System.out.println(sum); 

	}

}
